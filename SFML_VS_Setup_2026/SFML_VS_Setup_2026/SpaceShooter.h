enum class SSState {
    WELCOME,
    ENTER_P1,
    ENTER_P2,       // only if PvP mode
    MODE_SELECT,    // choose game mode
    COUNTDOWN,
    PLAYING,
    GAME_OVER,
    FINAL_RESULTS
};

enum class SSMode {
    NONE,
    SINGLE_PLAYER,   // 1 player vs waves
    COMPETITIVE,     // 2 players compete for score
    COOP             // 2 players survive together
};

// ?? Enemy ?????????????????????????????????????????????????????????????????????
struct Enemy {
    sf::ConvexShape shape;
    float vx, vy;
    bool  alive;
    int   hp;
    int   type;   // 0=basic  1=fast  2=tank
    Enemy();
};

// ?? Bullet ????????????????????????????????????????????????????????????????????
struct Bullet {
    sf::CircleShape shape;
    float vx, vy;
    bool  alive;
    int   owner;  // 1 or 2
    Bullet();
};

// ?? Particle ??????????????????????????????????????????????????????????????????
struct Particle {
    sf::CircleShape shape;
    float vx, vy;
    float life;
    float maxLife;
    bool  alive;
    Particle();
};

// ?? Player ship ???????????????????????????????????????????????????????????????
struct Ship {
    sf::ConvexShape body;
    sf::ConvexShape flame;
    float x, y;
    float speed;
    int   hp;
    int   maxHp;
    float shootCooldown;
    bool  alive;
    int   score;
    char  name[32];
    sf::Color color;
    Ship();
};

// ?? Star (background) ?????????????????????????????????????????????????????????
struct Star {
    float x, y, speed, brightness;
};

