enum class SLState {
    WELCOME,
    ENTER_P1,
    ENTER_P2,
    ROLLING,        // dice roll animation
    PLAYING,        // waiting for player to press SPACE to roll
    MOVING,         // token animating to new position
    SNAKE_SLIDE,    // snake animation
    LADDER_CLIMB,   // ladder animation
    GAME_OVER,
    FINAL_RESULTS
};

struct SLPlayer {
    char  name[32];
    int   pos;          // 1-100 (0 = start)
    float animX, animY; // for smooth token movement
    float targetX, targetY;
    sf::Color color;
    int   wins;
    int   snakeHits;
    int   ladderClimbs;
    int   totalRolls;
};

// Snake: head -> tail (slides down)
struct Snake {
    int head, tail;
};

// Ladder: bottom -> top (climbs up)
struct Ladder {
    int bottom, top;
};

