#pragma once
#include <SFML/Graphics.hpp>
#include <SFML/Audio.hpp>

enum class HubState { INTRO, MORPHING, MENU };

// ?? Floating background particle ??????????????????????????????????????????????
struct HubParticle {
    float x, y, vx, vy, size, alpha, maxAlpha;
    sf::Color color;
};

// ?? One game card (4 games + 1 leaderboard = 5 total) ?????????????????????????
struct GameCard {
    float baseX, baseY, cardW, cardH;
    float hoverScale;       // 1.0 -> 0.94 on hover
    float sweepOffset;      // glow sweep X offset (0..cardW+200)
    bool  isHovered;
    sf::Color accentColor;
    GameCard();
};

class Hub {
private:
    sf::RenderWindow* window;
    sf::Font          font;
    HubState          state;

    // ?? Colors ???????????????????????????????????????????????????????????????
    sf::Color neonCyan, neonPink, neonYellow, neonGreen, neonOrange, darkBg;

    // ?? Cards  (0-3 = games, 4 = leaderboard) ????????????????????????????????
    static const int TOTAL_CARDS = 5;
    GameCard cards[TOTAL_CARDS];

    // ?? Game thumbnails ???????????????????????????????????????????????????????
    sf::Texture gameTextures[4];
    sf::Sprite  gameSprites[4];
    bool        textureLoaded[4];

    // ?? Background particles ??????????????????????????????????????????????????
    static const int PARTICLE_COUNT = 80;
    HubParticle particles[PARTICLE_COUNT];

    // ?? Screen-corner HUD brackets ????????????????????????????????????????????
    sf::RectangleShape bracketsH[4];
    sf::RectangleShape bracketsV[4];

    // ?? Title animation ???????????????????????????????????????????????????????
    sf::Text  titleText, subtitleText;
    sf::RectangleShape titleUnderline;
    float glowTimer;
    float morphTimer;
    float cardRevealTimer;
    bool  cardsVisible;

    sf::Music bgMusic;

    // ?? Click sound ???????????????????????????????????????????????????????????
    sf::SoundBuffer bufClick;
    sf::Sound       sndClick;

    static const float MORPH_DURATION;
    float titleStartY, titleEndY;
    float titleStartSize, titleEndSize;

    // ?? Internal methods ??????????????????????????????????????????????????????
    void initParticles();
    void initCards();
    void initTextures();
    void initCornerBrackets();
    void initTitle();

    void updateParticles(float dt);
    void updateHover(sf::Vector2i mp, float dt);

    void drawBackground();
    void drawParticles();
    void drawCornerBrackets();

    // Card drawing
    void drawThickLine(float x1, float y1, float x2, float y2, float t, sf::Color c);
    void drawCardBrackets(float x, float y, float w, float h, sf::Color c, float alpha);
    void drawRoundedRect(float x, float y, float w, float h, float r, sf::Color fill, sf::RenderStates rs);
    void drawRoundedBorder(float x, float y, float w, float h, float r, float thick, sf::Color col, sf::RenderStates rs);
    void drawGameCard(int i, float alpha);
    void drawLBCard(float alpha);
    void drawIconForGame(int i, float px, float py, float pw, float ph);

    void drawIntro();
    void drawMenu(float alpha);

    float easeOutCubic(float t);
    float fabs2(float v) { return v < 0.f ? -v : v; }

public:
    Hub(sf::RenderWindow* win);
    int run();  // 0-3=game  4=leaderboard  -1=closed
};