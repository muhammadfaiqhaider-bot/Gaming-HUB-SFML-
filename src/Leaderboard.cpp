// ??????????????????????????????????????????????????????????????????????????????
//  String helpers  (no cstring / string libraries)
// ??????????????????????????????????????????????????????????????????????????????
int  Leaderboard::lbLen(const char* s) { int i = 0; while (s[i]) i++; return i; }
void Leaderboard::lbCpy(char* d, const char* s) { int i = 0; while ((d[i] = s[i])) i++; }
void Leaderboard::lbCat(char* d, const char* s) {
    int i = lbLen(d), j = 0; while ((d[i++] = s[j++]));
}
void Leaderboard::lbIts(int v, char* buf) {
    if (v == 0) { buf[0] = '0'; buf[1] = '\0'; return; }
    char t[12]; int i = 0;
    bool neg = (v < 0); if (neg) v = -v;
    while (v > 0) { t[i++] = '0' + v % 10; v /= 10; }
    int start = 0;
    if (neg) { buf[0] = '-'; start = 1; }
    for (int j = 0; j < i; j++) buf[start + j] = t[i - 1 - j];
    buf[start + i] = '\0';
}
bool Leaderboard::lbEq(const char* a, const char* b) {
    int i = 0;
    while (a[i] && b[i]) { if (a[i] != b[i]) return false; i++; }
    return a[i] == '\0' && b[i] == '\0';
}

// ??????????????????????????????????????????????????????????????????????????????
//  LBRecord default constructor
// ??????????????????????????????????????????????????????????????????????????????
LBRecord::LBRecord() : wins(0), totalGames(0) {
    playerName[0] = '\0';
    gameName[0] = '\0';
}

// ??????????????????????????????????????????????????????????????????????????????
//  Leaderboard constructor
// ??????????????????????????????????????????????????????????????????????????????
Leaderboard::Leaderboard(sf::RenderWindow* win)
    : window(win), count(0), glowTimer(0.f), scrollOffset(0)
{
    neonCyan = sf::Color(0, 240, 255);
    neonPink = sf::Color(255, 30, 180);
    neonYellow = sf::Color(255, 220, 0);
    neonGreen = sf::Color(0, 255, 120);
    neonOrange = sf::Color(255, 140, 0);
    darkBg = sf::Color(5, 5, 18);

    font.loadFromFile("Orbitron-VariableFont_wght.ttf");
}

// ??????????????????????????????????????????????????????????????????????????????
//  AddRecord
//  Finds existing entry for same player+game and updates it,
//  or appends a new record. Then re-sorts.
// ??????????????????????????????????????????????????????????????????????????????
void Leaderboard::AddRecord(const char* playerName,
    const char* gameName,
    int wins, int totalGames)
{
    // Search for existing record for this player + game
    for (int i = 0; i < count; i++) {
        if (lbEq(records[i].playerName, playerName) &&
            lbEq(records[i].gameName, gameName))
        {
            // Update: accumulate wins and games
            records[i].wins += wins;
            records[i].totalGames += totalGames;
            SortRecords();
            return;
        }
    }

    // New record
    if (count < MAX_RECORDS) {
        lbCpy(records[count].playerName, playerName);
        lbCpy(records[count].gameName, gameName);
        records[count].wins = wins;
        records[count].totalGames = totalGames;
        count++;
        SortRecords();
    }
}

// ??????????????????????????????????????????????????????????????????????????????
//  LoadFromFile
//  Format per line:  PLAYERNAME|GAMENAME|WINS|TOTALGAMES
//  Handles missing file gracefully (no crash).
// ??????????????????????????????????????????????????????????????????????????????
void Leaderboard::LoadFromFile(const char* filename)
{
    std::ifstream f(filename);
    if (!f.is_open()) return;   // file doesn't exist yet — that's fine

    count = 0;
    char line[128];

    while (f.getline(line, 128) && count < MAX_RECORDS)
    {
        // Parse: PLAYERNAME|GAMENAME|WINS|TOTALGAMES
        char* p = line;

        // Field 1: playerName
        int fi = 0;
        while (*p && *p != '|' && fi < 31)
            records[count].playerName[fi++] = *p++;
        records[count].playerName[fi] = '\0';
        if (*p == '|') p++;

        // Field 2: gameName
        fi = 0;
        while (*p && *p != '|' && fi < 31)
            records[count].gameName[fi++] = *p++;
        records[count].gameName[fi] = '\0';
        if (*p == '|') p++;

        // Field 3: wins (parse integer manually)
        int val = 0; bool neg = false;
        if (*p == '-') { neg = true; p++; }
        while (*p >= '0' && *p <= '9') { val = val * 10 + (*p - '0'); p++; }
        records[count].wins = neg ? -val : val;
        if (*p == '|') p++;

        // Field 4: totalGames
        val = 0; neg = false;
        if (*p == '-') { neg = true; p++; }
        while (*p >= '0' && *p <= '9') { val = val * 10 + (*p - '0'); p++; }
        records[count].totalGames = neg ? -val : val;

        // Only store if player name is non-empty
        if (records[count].playerName[0] != '\0')
            count++;
    }

    f.close();
    SortRecords();
}

// ??????????????????????????????????????????????????????????????????????????????
//  SaveToFile
//  Writes all records back in the same pipe-delimited format.
// ??????????????????????????????????????????????????????????????????????????????
void Leaderboard::SaveToFile(const char* filename)
{
    std::ofstream f(filename);
    if (!f.is_open()) return;

    for (int i = 0; i < count; i++) {
        f << records[i].playerName << '|'
            << records[i].gameName << '|'
            << records[i].wins << '|'
            << records[i].totalGames << '\n';
    }

    f.close();
}

// ??????????????????????????????????????????????????????????????????????????????
//  SortRecords  — insertion sort descending by wins
// ??????????????????????????????????????????????????????????????????????????????
void Leaderboard::SortRecords()
{
    for (int i = 1; i < count; i++) {
        LBRecord key = records[i];
        int j = i - 1;
        while (j >= 0 && records[j].wins < key.wins) {
            records[j + 1] = records[j];
            j--;
        }
        records[j + 1] = key;
    }
}

// ??????????????????????????????????????????????????????????????????????????????
//  FilterByGame  — fills out[] with records matching gameName
// ??????????????????????????????????????????????????????????????????????????????
void Leaderboard::FilterByGame(const char* gameName,
    LBRecord* out, int& outCount) const
{
    outCount = 0;
    for (int i = 0; i < count; i++) {
        if (lbEq(records[i].gameName, gameName)) {
            out[outCount++] = records[i];
        }
    }
}

// ??????????????????????????????????????????????????????????????????????????????
//  Drawing helpers
// ??????????????????????????????????????????????????????????????????????????????
void Leaderboard::drawBackground() {
    window->clear(darkBg);

    // Subtle grid
    sf::Color gridCol(255, 255, 255, 8);
    for (int x = 0; x < 1200; x += 60) {
        sf::RectangleShape l({ 1.f,900.f });
        l.setPosition((float)x, 0.f);
        l.setFillColor(gridCol);
        window->draw(l);
    }
    for (int y = 0; y < 900; y += 60) {
        sf::RectangleShape l({ 1200.f,1.f });
        l.setPosition(0.f, (float)y);
        l.setFillColor(gridCol);
        window->draw(l);
    }

    // Scanlines
    for (int i = 0; i < 150; i++) {
        sf::RectangleShape s({ 1200.f,1.f });
        s.setPosition(0.f, (float)(i * 6));
        s.setFillColor(sf::Color(0, 0, 0, 18));
        window->draw(s);
    }
}

void Leaderboard::centeredText(const char* str, float y, unsigned sz,
    sf::Color col, bool bold)
{
    sf::Text t;
    t.setFont(font); t.setString(str);
    t.setCharacterSize(sz); t.setFillColor(col);
    if (bold) t.setStyle(sf::Text::Bold);
    sf::FloatRect b = t.getLocalBounds();
    t.setOrigin(b.width / 2.f, b.height / 2.f);
    t.setPosition(600.f, y);
    window->draw(t);
}

void Leaderboard::drawRoundedRect(float x, float y, float w, float h,
    float r, sf::Color fill,
    sf::Color outline, float thick)
{
    // Simple sharp-cornered rect — no circles
    sf::RectangleShape box({ w,h });
    box.setPosition(x, y);
    box.setFillColor(fill);
    if (thick > 0.f) {
        box.setOutlineThickness(thick);
        box.setOutlineColor(outline);
    }
    window->draw(box);
}

// ??????????????????????????????????????????????????????????????????????????????
//  DisplayLeaderboard  — full graphical SFML screen
// ??????????????????????????????????????????????????????????????????????????????
void Leaderboard::DisplayLeaderboard()
{

    const char* tabNames[5] = { "ALL","TIC TAC TOE","PONG","SNAKE & LADDER","SPACE SHOOTER" };
    const char* gameKeys[5] = { "",   "TicTacToe", "Pong","SnakeLadder",   "SpaceShooter" };
    sf::Color   tabCols[5] = { neonYellow, neonCyan, neonPink, neonYellow, neonGreen };
    int activeTab = 0;
    scrollOffset = 0;
    glowTimer = 0.f;
    sf::Clock clock;

    const float ROW_H = 54.f;
    const float TABLE_X = 60.f;
    const float TABLE_Y = 230.f;
    const float TABLE_W = 1080.f;
    const int   ROWS_VIS = 10;   // rows visible at once

    while (window->isOpen())
    {
        float dt = clock.restart().asSeconds();
        glowTimer += dt;
        float pulse = (std::sin(glowTimer * 2.f) + 1.f) / 2.f;

        sf::Event ev;
        while (window->pollEvent(ev)) {
            if (ev.type == sf::Event::Closed) { window->close(); return; }

            if (ev.type == sf::Event::KeyPressed) {
                if (ev.key.code == sf::Keyboard::Escape) { return; }

                // Tab switching  1-5
                if (ev.key.code == sf::Keyboard::Num1 || ev.key.code == sf::Keyboard::Numpad1)
                {
                    activeTab = 0; scrollOffset = 0;
                }
                if (ev.key.code == sf::Keyboard::Num2 || ev.key.code == sf::Keyboard::Numpad2)
                {
                    activeTab = 1; scrollOffset = 0;
                }
                if (ev.key.code == sf::Keyboard::Num3 || ev.key.code == sf::Keyboard::Numpad3)
                {
                    activeTab = 2; scrollOffset = 0;
                }
                if (ev.key.code == sf::Keyboard::Num4 || ev.key.code == sf::Keyboard::Numpad4)
                {
                    activeTab = 3; scrollOffset = 0;
                }
                if (ev.key.code == sf::Keyboard::Num5 || ev.key.code == sf::Keyboard::Numpad5)
                {
                    activeTab = 4; scrollOffset = 0;
                }

                // Scroll
                if (ev.key.code == sf::Keyboard::Down) scrollOffset++;
                if (ev.key.code == sf::Keyboard::Up && scrollOffset > 0) scrollOffset--;
            }
        }

        // ?? Build display list for active tab ????????????????????????????????
        LBRecord display[MAX_RECORDS];
        int      dCount = 0;

        if (activeTab == 0) {
            for (int i = 0; i < count; i++) display[dCount++] = records[i];
        }
        else {
            FilterByGame(gameKeys[activeTab], display, dCount);
        }

        // Clamp scroll
        int maxScroll = dCount - ROWS_VIS;
        if (maxScroll < 0) maxScroll = 0;
        if (scrollOffset > maxScroll) scrollOffset = maxScroll;

        // ?? Draw ?????????????????????????????????????????????????????????????
        drawBackground();

        // Title
        float uw = 500.f + 50.f * pulse;
        sf::RectangleShape ul({ uw,3.f });
        ul.setFillColor(neonYellow);
        ul.setOrigin(uw / 2.f, 1.5f); ul.setPosition(600.f, 88.f);
        window->draw(ul);
        centeredText("LEADERBOARD", 58.f, 46, neonYellow, true);

        // ?? Tab bar ??????????????????????????????????????????????????????????
        float tabW = 196.f, tabH = 38.f;
        float tabStartX = (1200.f - 5 * tabW - 4 * 8.f) / 2.f;
        for (int t = 0; t < 5; t++) {
            float tx = tabStartX + t * (tabW + 8.f);
            float ty = 108.f;
            bool  sel = (t == activeTab);

            sf::Color tc = tabCols[t];
            sf::Color bg = sel ?
                sf::Color(tc.r / 6, tc.g / 6, tc.b / 6, 230) :
                sf::Color(10, 10, 28, 180);
            sf::Color out = sel ?
                tc :
                sf::Color(tc.r, tc.g, tc.b, 60);

            drawRoundedRect(tx, ty, tabW, tabH, 8.f, bg, out, sel ? 2.5f : 1.f);

            // Tab number hint
            char numBuf[4] = { '[','1' + (char)t,']','\0' };
            sf::Text numT;
            numT.setFont(font); numT.setString(numBuf);
            numT.setCharacterSize(9);
            numT.setFillColor(sf::Color(tc.r, tc.g, tc.b, sel ? 180 : 80));
            numT.setPosition(tx + 6.f, ty + 4.f);
            window->draw(numT);

            sf::Text lbl;
            lbl.setFont(font); lbl.setString(tabNames[t]);
            lbl.setCharacterSize(sel ? 12 : 11);
            lbl.setFillColor(sel ? tc : sf::Color(tc.r, tc.g, tc.b, 140));
            if (sel) lbl.setStyle(sf::Text::Bold);
            sf::FloatRect lb = lbl.getLocalBounds();
            lbl.setOrigin(lb.width / 2.f, lb.height / 2.f);
            lbl.setPosition(tx + tabW / 2.f, ty + tabH / 2.f + 2.f);
            window->draw(lbl);
        }

        // ?? Table header ?????????????????????????????????????????????????????
        drawRoundedRect(TABLE_X, TABLE_Y - 4.f, TABLE_W, 40.f, 6.f,
            sf::Color(20, 20, 50, 200),
            sf::Color(neonCyan.r, neonCyan.g, neonCyan.b, 60), 1.5f);

        float colX[5] = { TABLE_X + 20, TABLE_X + 90, TABLE_X + 450,
                          TABLE_X + 720, TABLE_X + 900 };
        const char* headers[5] = { "#","PLAYER","GAME","WINS","GAMES" };
        sf::Color hCol(150, 150, 200, 200);
        for (int c = 0; c < 5; c++) {
            sf::Text ht;
            ht.setFont(font); ht.setString(headers[c]);
            ht.setCharacterSize(12);
            ht.setFillColor(hCol);
            ht.setStyle(sf::Text::Bold);
            ht.setPosition(colX[c], TABLE_Y + 8.f);
            window->draw(ht);
        }

        // ?? Table rows ???????????????????????????????????????????????????????
        sf::Color rankCols[3] = {
            sf::Color(255,210,0),   // gold
            sf::Color(180,180,180), // silver
            sf::Color(200,120,60)   // bronze
        };

        for (int row = 0; row < ROWS_VIS; row++) {
            int idx = row + scrollOffset;
            float ry = TABLE_Y + 40.f + row * ROW_H;

            // Alternating row background
            sf::Color rowBg = (row % 2 == 0) ?
                sf::Color(14, 14, 38, 180) :
                sf::Color(10, 10, 28, 140);

            drawRoundedRect(TABLE_X, ry, TABLE_W, ROW_H - 4.f, 6.f,
                rowBg, sf::Color(255, 255, 255, 12), 1.f);

            if (idx >= dCount) {
                // Empty row placeholder
                continue;
            }

            LBRecord& rec = display[idx];

            // Rank column
            char rankBuf[8]; lbIts(idx + 1, rankBuf);
            sf::Text rankT;
            rankT.setFont(font);
            rankT.setString(rankBuf);
            rankT.setCharacterSize(16);
            sf::Color rc = (idx < 3) ? rankCols[idx] : sf::Color(100, 100, 130);
            rankT.setFillColor(rc);
            if (idx < 3) rankT.setStyle(sf::Text::Bold);
            rankT.setPosition(colX[0], ry + ROW_H / 2.f - 10.f);
            window->draw(rankT);

            // Medal for top 3
            if (idx < 3) {
                sf::CircleShape medal(10.f, 6);
                medal.setOrigin(10.f, 10.f);
                medal.setPosition(colX[0] + 30.f, ry + ROW_H / 2.f - 2.f);
                medal.setFillColor(sf::Color(rc.r / 4, rc.g / 4, rc.b / 4, 180));
                medal.setOutlineThickness(1.5f);
                medal.setOutlineColor(rc);
                window->draw(medal);
            }

            // Player name
            sf::Text nameT;
            nameT.setFont(font); nameT.setString(rec.playerName);
            nameT.setCharacterSize(15);
            nameT.setFillColor(sf::Color(220, 220, 255));
            nameT.setStyle(sf::Text::Bold);
            nameT.setPosition(colX[1], ry + ROW_H / 2.f - 10.f);
            window->draw(nameT);

            // Game name — colored by game
            sf::Color gc2 = neonCyan;
            if (lbEq(rec.gameName, "Pong"))          gc2 = neonPink;
            else if (lbEq(rec.gameName, "SnakeLadder")) gc2 = neonYellow;
            else if (lbEq(rec.gameName, "SpaceShooter")) gc2 = neonGreen;

            sf::Text gameT;
            gameT.setFont(font); gameT.setString(rec.gameName);
            gameT.setCharacterSize(13);
            gameT.setFillColor(sf::Color(gc2.r, gc2.g, gc2.b, 200));
            gameT.setPosition(colX[2], ry + ROW_H / 2.f - 8.f);
            window->draw(gameT);

            // Wins — big and bright
            char winBuf[16]; lbIts(rec.wins, winBuf);
            sf::Text winsT;
            winsT.setFont(font); winsT.setString(winBuf);
            winsT.setCharacterSize(18);
            winsT.setFillColor(neonGreen);
            winsT.setStyle(sf::Text::Bold);
            winsT.setPosition(colX[3], ry + ROW_H / 2.f - 11.f);
            window->draw(winsT);

            // Total games
            char tgBuf[16]; lbIts(rec.totalGames, tgBuf);
            sf::Text tgT;
            tgT.setFont(font); tgT.setString(tgBuf);
            tgT.setCharacterSize(14);
            tgT.setFillColor(sf::Color(140, 140, 180));
            tgT.setPosition(colX[4], ry + ROW_H / 2.f - 9.f);
            window->draw(tgT);
        }

        // ?? Empty state ???????????????????????????????????????????????????????
        if (dCount == 0) {
            centeredText("NO RECORDS YET", 570.f, 22,
                sf::Color(80, 80, 110), false);
            centeredText("Play a game to add your score!",
                610.f, 14, sf::Color(60, 60, 90), false);
        }

        // ?? Scroll indicator ?????????????????????????????????????????????????
        if (dCount > ROWS_VIS) {
            char scBuf[48];
            scBuf[0] = '\0';
            lbCat(scBuf, "Showing ");
            char tmp[8]; lbIts(scrollOffset + 1, tmp); lbCat(scBuf, tmp);
            lbCat(scBuf, "-");
            int last = scrollOffset + ROWS_VIS;
            if (last > dCount) last = dCount;
            lbIts(last, tmp); lbCat(scBuf, tmp);
            lbCat(scBuf, " of ");
            lbIts(dCount, tmp); lbCat(scBuf, tmp);
            lbCat(scBuf, "   [UP/DOWN] scroll");

            centeredText(scBuf, 796.f, 12, sf::Color(100, 100, 140));
        }

        // ?? Bottom bar ????????????????????????????????????????????????????????
        drawRoundedRect(0.f, 820.f, 1200.f, 80.f, 0.f,
            sf::Color(8, 8, 24, 220),
            sf::Color(neonCyan.r, neonCyan.g, neonCyan.b, 40), 1.f);

        sf::Text hint;
        hint.setFont(font);
        hint.setString("[1-5] Filter by game    [UP / DOWN] Scroll    [ESC] Back to Hub");
        hint.setCharacterSize(13);
        hint.setFillColor(sf::Color(100, 100, 150));
        sf::FloatRect hb = hint.getLocalBounds();
        hint.setOrigin(hb.width / 2.f, hb.height / 2.f);
        hint.setPosition(600.f, 858.f);
        window->draw(hint);

        window->display();
    }
}